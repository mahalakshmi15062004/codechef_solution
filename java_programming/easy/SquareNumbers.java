public class SquareNumbers {
    public static void main(String[] args) {
        // Loop from 1 to 5
        for (int i = 1; i <= 5; i++) {
            // Calculate the square using multiplication
            int square = i * i;
            
            // Print the result in the requested format
            System.out.println(i + " - " + square);
        }
    }
}
